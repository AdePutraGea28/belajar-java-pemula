package belajar_menampilkan_data_di_terminal.tanpa_vaiabel;

public class PrintApp {
    public static void main(String[] args) {
        // ini contoh penggunaan output dengan string
        System.out.println("Hello Word");

        // ini contoh penggunaan output dengan integer
        System.out.println(1);

        // ini contoh penggunaan output dengan floating point
        System.out.println(1.1);

        // ini contoh penggunaan output dengan boolean
        System.out.println(true);

        // ini contoh penggunaan output dengan character
        System.out.println('A');
    }
}
